package Q5;

public class Teclado {
    private int numTecla;
    private String tipo;

    public int getNumTecla() {
        return numTecla;
    }

    public void setNumTecla(int numTecla) {
        this.numTecla = numTecla;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}

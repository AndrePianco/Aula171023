package ClassesQ4;

public class Contato {
    private String telefResidenci;
    private String Celular;
    private String email;

    public Contato(){

    }

    public String getTelefResidenci() {
        return telefResidenci;
    }
    public void setTelefResidenci(String telefResidenci) {
        this.telefResidenci = telefResidenci;
    }
    public String getCelular() {
        return Celular;
    }
    public void setCelular(String celular) {
        Celular = celular;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }

}
